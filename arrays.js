
/**
 * Demonstrates the usage of the Array.find() method.
 * Creates an array 'a' with numbers and finds the first occurrence of number 2.
 * @example
 * const a = [1, 2, 3, 2, 4];
 * const result = a.find((element) => element === 2);
 * console.log(result); // Output: 2
 */
const a = [1, 2, 3, 2, 4];
const result = a.find((element) => element === 2);
console.log(result); // Output: 2